package Day03;

public class OperDemo5 {
    public static void main(String[] args) {
        //5.字符串连接运算符:+
        int age = 40;
        System.out.println("age="); //age=
        System.out.println(age); //40
        System.out.println("age="+age); //age=40

        String name = "CC"; //声明字符串型变量name
        System.out.println("大家好,我叫"+name);
        System.out.println("-----------------------");

    }
}
