package homework_h.c02.ooDay04;

public class Point {
    private int x;
    private int y;
    public Point(){
    }
    public int getX(){
        return x;
    }
    public int getY(){
        return y;
    }
    public void setX(int x){
        this.x = x;
    }
    public void setY(int y){
        this.y = y;
    }

}
