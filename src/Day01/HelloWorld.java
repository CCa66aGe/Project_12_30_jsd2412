package Day01;  //声明包Day01
//声明类HelloWorld
public class HelloWorld {
    //主方法,为程序的入口,运行程序时自动执行main
    public static void main(String[] args) {
        //输出Helloworld
        //1)严格区分大小写
        //2)所有符号必须是英文模式
        //3)每句话必须以分号结尾
        System.out.println("HelloWorld");//双引号原样输出
        System.out.println(2);//输出数字可以不加双引号
        System.out.println("java");
        System.out.println("Hello");
        System.out.println("1\\");
        
    }
}
